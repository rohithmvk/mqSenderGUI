package com.infy.MQSenderGUI;

public class MQSenderDAO {
	private String InputIP;
	private String InputChannel;
	private String InputQMgr;
	private String InputMQ;
	private String InputMessage;
	private String InputPort;
	public String getInputIP() {
		return InputIP;
	}
	public void setInputIP(String inputIP) {
		InputIP = inputIP;
	}
	public String getInputChannel() {
		return InputChannel;
	}
	public void setInputChannel(String inputChannel) {
		InputChannel = inputChannel;
	}
	public String getInputQMgr() {
		return InputQMgr;
	}
	public void setInputQMgr(String inputQMgr) {
		InputQMgr = inputQMgr;
	}
	public String getInputMQ() {
		return InputMQ;
	}
	public void setInputMQ(String inputMQ) {
		InputMQ = inputMQ;
	}
	public String getInputMessage() {
		return InputMessage;
	}
	public void setInputMessage(String inputMessage) {
		InputMessage = inputMessage;
	}
	public void setInputPort(String inputPort) {
		InputPort = inputPort;
	}
	public String getInputPort() {
		return InputPort;
	}
}
